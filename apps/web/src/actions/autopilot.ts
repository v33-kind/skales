// ─── LLM Helper ───────────────────────────────────────────────────────────────

async function callLLM(settings: any, systemPrompt: string, userMessage: string): Promise<string> {
    const provider = settings?.activeProvider ?? 'openrouter';
    const provCfg  = settings?.providers?.[provider] ?? {};
    const model    = provCfg.model ?? 'gpt-4o';
    const apiKey   = provCfg.apiKey ?? '';

    if (!apiKey) throw new Error(`No API key configured for provider "${provider}".`);

    // Anthropic
    if (provider === 'anthropic') {
        const res = await fetch('https://api.anthropic.com/v1/messages', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json', 'x-api-key': apiKey, 'anthropic-version': '2023-06-01' },
            body: JSON.stringify({ model, max_tokens: 2048, system: systemPrompt, messages: [{ role: 'user', content: userMessage }] }),
        });
        if (!res.ok) throw new Error(`Anthropic ${res.status}`);
        const d = await res.json();
        return d.content?.[0]?.text?.trim() ?? '';
    }

    // Google
    if (provider === 'google') {
        const url  = `https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent?key=${apiKey}`;
        const res  = await fetch(url, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ contents: [{ parts: [{ text: systemPrompt + '\n\n' + userMessage }] }] }),
        });
        if (!res.ok) throw new Error(`Google ${res.status}`);
        const d = await res.json();
        return d.candidates?.[0]?.content?.parts?.[0]?.text?.trim() ?? '';
    }

    // OpenAI-compatible
    const endpoints: Record<string, string> = {
        openrouter: 'https://openrouter.ai/api/v1/chat/completions',
        openai:     'https://api.openai.com/v1/chat/completions',
        groq:       'https://api.groq.com/openai/v1/chat/completions',
        mistral:    'https://api.mistral.ai/v1/chat/completions',
        deepseek:   'https://api.deepseek.com/v1/chat/completions',
        xai:        'https://api.x.ai/v1/chat/completions',
        together:   'https://api.together.xyz/v1/chat/completions',
        ollama:     (provCfg.baseUrl ?? 'http://localhost:11434/v1') + '/chat/completions',
    };
    const url = endpoints[provider] ?? endpoints.openrouter;
    const hdrs: Record<string, string> = { 'Content-Type': 'application/json' };
    if (apiKey && apiKey.trim() !== '') { hdrs['Authorization'] = `Bearer ${apiKey.trim()}`; }
    if (provider === 'openrouter') { hdrs['HTTP-Referer'] = 'https://skales.app'; hdrs['X-Title'] = 'Skales Autopilot'; }

    const res = await fetch(url, {
        method: 'POST',
        headers: hdrs,
        body: JSON.stringify({ model, max_tokens: 2048, temperature: 0.4, messages: [
            { role: 'system', content: systemPrompt },
            { role: 'user',   content: userMessage },
        ]}),
    });
    if (!res.ok) throw new Error(`${provider} ${res.status}: ${await res.text()}`);
    const d   = await res.json();
    const txt = d.choices?.[0]?.message?.content?.trim();
    if (!txt) throw new Error(`Empty response from ${provider}.`);
    return txt;
}

// ─── Deep-Dive Interview ──────────────────────────────────────────────────────

/**
 * Run one turn of the guided Deep-Dive interview.
 * Pass the full conversation history; receives the next AI question or a
 * special COMPLETE marker when enough information has been collected.
 *
 * Returns:
 *   { turn: 'question', message: string }  — follow-up question
 *   { turn: 'complete', profile: UserProfile, summary: string } — interview done
 */
export async function runInterviewTurn(options: {
    history: { role: 'user' | 'assistant'; content: string }[];
    activeSkills?: string[];
}): Promise<{
    success: boolean;
    turn?:   'question' | 'complete';
    message?: string;
    profile?: UserProfile;
    summary?: string;
    error?:   string;
}> {
    try {
        const settings     = await loadSettings();
        const activeSkills = options.activeSkills ?? [];

        const systemPrompt = `You are Skales' Chief-of-Staff AI conducting a strategic Deep-Dive interview to understand the user's goals deeply.

INTERVIEW RULES:
1. Ask ONE focused question at a time. Never multiple questions in one message.
2. Gather: primary goal, niche, budget/resources, constraints, timeline, and any prior attempts.
3. After 4-6 exchanges (when you have enough to build a comprehensive plan), output EXACTLY this JSON block and nothing else:

\`\`\`json
{
  "COMPLETE": true,
  "primaryGoal": "...",
  "niche": "...",
  "budget": "...",
  "constraints": "...",
  "additionalContext": "...",
  "masterPlanSummary": "3-5 sentence strategic overview"
}
\`\`\`

4. Before COMPLETE, respond ONLY with your next interview question as plain text.
5. Be encouraging, direct, and professional. This is a premium feature.
${activeSkills.length ? `\nACTIVE SKILLS (relevant for planning): ${activeSkills.join(', ')}` : ''}`;

        const raw = await callLLM(settings, systemPrompt,
            options.history.map(m => `${m.role === 'user' ? 'User' : 'Skales'}: ${m.content}`).join('\n'),
        );

        // Detect COMPLETE marker
        const jsonMatch = raw.match(/```json\s*([\s\S]*?)```/);
        if (jsonMatch) {
            let parsed: any = {};
            try { parsed = JSON.parse(jsonMatch[1]); } catch { /* ignore */ }

            if (parsed.COMPLETE) {
                const profile: UserProfile = {
                    primaryGoal:       parsed.primaryGoal,
                    niche:             parsed.niche,
                    budget:            parsed.budget,
                    constraints:       parsed.constraints,
                    additionalContext: parsed.additionalContext,
                    masterPlan:        parsed.masterPlanSummary,
                };
                await saveUserProfile(profile);
                return { success: true, turn: 'complete', profile, summary: parsed.masterPlanSummary };
            }
        }

        return { success: true, turn: 'question', message: raw.replace(/```json[\s\S]*?```/g, '').trim() };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

// ─── Master Plan Generation ────────────────────────────────────────────────────

/**
 * Generate a Master Plan from the user's profile + active skills.
 * Breaks it into actionable sub-tasks and pushes them to tasks.json.
 *
 * Returns the list of created tasks + a high-level roadmap string.
 */
export async function generateMasterPlan(options?: {
    activeSkills?: string[];
    overrideGoal?: string;
}): Promise<{
    success:   boolean;
    tasks?:    AgentTask[];
    roadmap?:  string;
    planTitle?: string;
    error?:    string;
}> {
    try {
        const settings = await loadSettings();
        const profile  = await loadUserProfile();
        const goal     = options?.overrideGoal ?? profile.primaryGoal ?? 'Build a successful online business';
        const skills   = options?.activeSkills ?? [];

        const systemPrompt = `You are Skales' strategic AI planner. Your job is to generate a deterministic, actionable Master Plan.

USER PROFILE:
Goal: ${goal}
Niche: ${profile.niche ?? 'not specified'}
Budget: ${profile.budget ?? 'not specified'}
Constraints: ${profile.constraints ?? 'none'}
Context: ${profile.additionalContext ?? 'none'}

AVAILABLE SKILLS: ${skills.length ? skills.join(', ') : 'standard (chat, file management, web search)'}

OUTPUT FORMAT (respond with ONLY valid JSON — no markdown fences, no extra text):
{
  "planTitle": "Short 5-word plan name",
  "roadmap": "3-5 sentence high-level strategy",
  "tasks": [
    {
      "title": "Concise action title (max 80 chars)",
      "description": "Detailed instructions for the AI agent executing this task",
      "priority": "high|normal|low",
      "tags": ["tag1", "tag2"],
      "schedule": null
    }
  ]
}

For each task, decide if it is ONE-TIME or RECURRING.
- ONE-TIME tasks: set "schedule": null
- RECURRING tasks: set "schedule" to a standard 5-field cron expression.
  Examples:
    "0 8 * * *"     = every day at 8:00 AM
    "0 9 * * 1"     = every Monday at 9:00 AM
    "*/30 * * * *"  = every 30 minutes
    "0 8,18 * * *"  = every day at 8:00 AM and 6:00 PM
    "0 9 * * 1-5"   = every weekday at 9:00 AM

RULES:
- Generate 5-12 concrete, executable tasks ordered by logical sequence
- Each task must be completable by an AI agent without human input (or clearly state what human input is needed)
- Do NOT repeat tasks. Be specific, not vague.
- Tasks must directly serve the stated goal.
- If the goal implies daily/weekly/recurring work (monitoring, reporting, checking), use "schedule" with the appropriate cron expression.`;

        const raw = await callLLM(settings, systemPrompt, `Generate a complete Master Plan to: ${goal}`);

        // Parse JSON — strip any accidental markdown fences
        const cleaned = raw.replace(/^```json\s*/i, '').replace(/```\s*$/, '').trim();
        let parsed: any;
        try {
            parsed = JSON.parse(cleaned);
        } catch {
            // Attempt to extract JSON with regex
            const m = raw.match(/\{[\s\S]*\}/);
            if (!m) throw new Error('LLM did not return valid JSON for the Master Plan.');
            parsed = JSON.parse(m[0]);
        }

        const planTitle: string = parsed.planTitle ?? 'Master Plan';
        const roadmap:   string = parsed.roadmap   ?? '';
        const rawTasks: MasterPlanTask[] = Array.isArray(parsed.tasks) ? parsed.tasks : [];

        // Push tasks into tasks.json (one-shot) or cron_jobs (recurring)
        const created: AgentTask[] = [];
        let cronJobsCreated = 0;
        for (const t of rawTasks) {
            if (!t.title || !t.description) continue;

            // Recurring task → create a cron job instead of a one-shot task
            if (t.schedule && typeof t.schedule === 'string' && /^\S+\s+\S+\s+\S+\s+\S+\s+\S+$/.test(t.schedule.trim())) {
                try {
                    const { createCronJob } = await import('@/actions/tasks');
                    await createCronJob({
                        name:     t.title.slice(0, 200),
                        schedule: t.schedule.trim(),
                        task:     t.description,
                        enabled:  true,
                    });
                    cronJobsCreated++;
                } catch (cronErr: any) {
                    console.warn(`[MasterPlan] Failed to create cron job for "${t.title}":`, cronErr?.message);
                    // Fallback: create as one-shot task
                    const task = createTask({
                        title:       t.title.slice(0, 200),
                        description: t.description,
                        priority:    t.priority ?? 'normal',
                        source:      'autopilot',
                        tags:        t.tags ?? [],
                        planTitle,
                        maxRetries:  3,
                    });
                    created.push(task);
                }
            } else {
                // One-shot task (existing behavior)
                const task = createTask({
                    title:       t.title.slice(0, 200),
                    description: t.description,
                    priority:    t.priority ?? 'normal',
                    source:      'autopilot',
                    tags:        t.tags ?? [],
                    planTitle,
                    maxRetries:  3,
                });
                created.push(task);
            }
        }

        // Archive previous roadmap before overwriting
        try {
            const { loadUserProfile } = await import('./autopilot');
            const existing = await loadUserProfile();
            if (existing?.masterPlan) {
                const history: Array<{ roadmap: string; planTitle: string; createdAt: number }> =
                    Array.isArray(existing.roadmapHistory) ? existing.roadmapHistory : [];
                history.unshift({ roadmap: existing.masterPlan, planTitle: existing.masterPlanTitle || 'Plan', createdAt: typeof existing.updatedAt === 'number' ? existing.updatedAt : Date.now() });
                // Keep max 10 historical entries
                await saveUserProfile({ roadmapHistory: history.slice(0, 10), masterPlanTitle: planTitle });
            } else {
                await saveUserProfile({ masterPlanTitle: planTitle });
            }
        } catch { /* non-fatal — history tracking is best-effort */ }

        // Persist new roadmap to profile
        await saveUserProfile({ masterPlan: roadmap });

        const { log } = await import('@/lib/autopilot-logger');
        const cronNote = cronJobsCreated > 0 ? ` + ${cronJobsCreated} recurring schedule(s)` : '';
        log.success('plan_created', `🗺️ Master Plan "${planTitle}" created with ${created.length} tasks${cronNote}.`, {
            detail: { planTitle, taskCount: created.length, cronJobsCreated },
        });

        return { success: true, tasks: created, roadmap, planTitle };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

// ─── Daily Stand-Up Report ────────────────────────────────────────────────────

/**
 * Generate a concise stand-up report of what the agent did + what's blocked.
 * Returns a markdown string suitable for display in the chat or dashboard.
 */
export async function generateStandupReport(): Promise<{
    success: boolean;
    report?: string;
    hasBlockedTasks?: boolean;
    error?: string;
}> {
    try {
        const settings = await loadSettings();
        const tasks    = getAllTasks();
        const logs     = getLogs({ limit: 50 });

        const completed = tasks.filter(t => t.state === 'completed').slice(0, 10);
        const blocked   = tasks.filter(t => t.state === 'blocked');
        const pending   = tasks.filter(t => t.state === 'pending');

        if (completed.length === 0 && blocked.length === 0) {
            return { success: true, report: 'No completed or blocked tasks to report.', hasBlockedTasks: false };
        }

        const systemPrompt = `You are Skales generating a crisp daily stand-up report for the user.
Write in first person ("I completed...", "I am blocked on...").
Be friendly but professional. Use bullet points. Keep it under 200 words.
End with a clear question if there are blocked tasks.`;

        const userMessage = `
COMPLETED TASKS (${completed.length}):
${completed.map(t => `• ${t.title}: ${(t.result ?? '').slice(0, 150)}`).join('\n') || 'None'}

BLOCKED TASKS (${blocked.length}):
${blocked.map(t => `• ${t.title}: ${t.blockedReason ?? t.error ?? 'Unknown reason'}`).join('\n') || 'None'}

PENDING TASKS (${pending.length}):
${pending.slice(0, 5).map(t => `• ${t.title} [${t.priority}]`).join('\n') || 'None'}

RECENT LOG ACTIVITY:
${logs.slice(0, 5).map(l => `• ${l.timestamp.slice(11, 16)} — ${l.message}`).join('\n') || 'None'}

Generate the stand-up report now.`;

        const report = await callLLM(settings, systemPrompt, userMessage);

        const { log } = await import('@/lib/autopilot-logger');
        log.info('standup_generated', `📋 Daily stand-up generated.`, { detail: { blockedCount: blocked.length, completedCount: completed.length } });

        return { success: true, report, hasBlockedTasks: blocked.length > 0 };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

// ─── Morning Briefing ─────────────────────────────────────────────────────────

/**
 * Generate a personalised morning briefing delivered via Telegram (if configured)
 * and returned as a markdown string for display in the dashboard.
 *
 * Sections (graceful degrades if data unavailable):
 *   1. Greeting + date/time
 *   2. Today's calendar events (try/catch — optional integration)
 *   3. Pending & in-progress tasks
 *   4. Unread email count (try/catch — optional integration)
 *   5. Closing motivational question from the LLM
 */
export async function generateMorningBriefing(): Promise<{
    success: boolean;
    briefing?: string;
    deliveredViaTelegram?: boolean;
    error?: string;
}> {
    try {
        const settings = await loadSettings();
        const tasks    = getAllTasks();

        // ── Gather context sections ──────────────────────────────────────────

        const now      = new Date();
        const dateStr  = now.toLocaleDateString('en-US', { weekday: 'long', month: 'long', day: 'numeric' });
        const timeStr  = now.toLocaleTimeString('en-US', { hour: '2-digit', minute: '2-digit' });

        // Pending / in-progress tasks
        const pending    = tasks.filter(t => t.state === 'pending').slice(0, 8);
        const inProgress = tasks.filter(t => t.state === 'running').slice(0, 3);
        const blocked    = tasks.filter(t => t.state === 'blocked');

        // Calendar events — optional (no getCalendarEventsToday yet)
        let calendarLines = '(Calendar integration not configured)';
        try {
            // @ts-ignore — dynamic import in case calendar module ships later
            const { getCalendarEventsToday } = await import('@/actions/calendar');
            const events: Array<{ time: string; title: string }> = await getCalendarEventsToday();
            if (events.length === 0) {
                calendarLines = 'No events scheduled for today.';
            } else {
                calendarLines = events.map(e => `• ${e.time} — ${e.title}`).join('\n');
            }
        } catch { /* calendar not available */ }

        // Unread email count — optional
        let emailLines = '(Email integration not configured)';
        try {
            // @ts-ignore — dynamic import in case email count helper ships later
            const { getUnreadEmailCount } = await import('@/actions/email');
            const count: number = await getUnreadEmailCount();
            emailLines = count === 0 ? 'Inbox zero! 🎉' : `${count} unread message${count !== 1 ? 's' : ''} waiting.`;
        } catch { /* email not available */ }

        // ── Build LLM prompt ─────────────────────────────────────────────────

        const systemPrompt = `You are Skales generating a warm, upbeat morning briefing for the user.
Write in second person ("You have...", "Today looks...").
Be concise, friendly, and motivating. Use light markdown (bold for key items, bullet points).
Keep it under 250 words. End with one short, encouraging question to help the user focus.`;

        const userMessage = `
TODAY: ${dateStr} at ${timeStr}

CALENDAR EVENTS:
${calendarLines}

TASKS IN PROGRESS (${inProgress.length}):
${inProgress.map(t => `• ${t.title}`).join('\n') || 'None'}

PENDING TASKS (${pending.length} shown):
${pending.map(t => `• ${t.title} [${t.priority ?? 'normal'}]`).join('\n') || 'None'}

BLOCKED TASKS (${blocked.length}):
${blocked.map(t => `• ${t.title}: ${t.blockedReason ?? 'Unknown reason'}`).join('\n') || 'None'}

EMAILS:
${emailLines}

Generate the morning briefing now.`;

        const briefing = await callLLM(settings, systemPrompt, userMessage);

        // ── Deliver via Telegram if configured ───────────────────────────────

        let deliveredViaTelegram = false;
        try {
            const { loadTelegramConfig, sendMessage } = await import('@/actions/telegram');
            const tgCfg = await loadTelegramConfig();
            if (tgCfg?.token && tgCfg?.chatId) {
                const header = `☀️ *Good morning — ${dateStr}*\n\n`;
                await sendMessage(tgCfg.token, tgCfg.chatId, header + briefing);
                deliveredViaTelegram = true;
            }
        } catch { /* non-fatal — Telegram may not be configured */ }

        const { log } = await import('@/lib/autopilot-logger');
        log.success('briefing_generated', `☀️ Morning briefing generated.`, {
            detail: { pendingCount: pending.length, blockedCount: blocked.length, deliveredViaTelegram },
        });

        return { success: true, briefing, deliveredViaTelegram };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

