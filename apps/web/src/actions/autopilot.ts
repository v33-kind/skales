export async function rejectAutopilotTask(id: string, reason?: string): Promise<{ success: boolean; error?: string }> {
    try {
        const { rejectTask } = await import('@/lib/agent-tasks');
        const { log } = await import('@/lib/autopilot-logger');
        rejectTask(id, reason);
        log.warning('task_cancelled', `❌ Task rejected by user: ${reason ?? 'No reason given'}.`, { taskId: id });
        return { success: true };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

/**
 * Retry a blocked task — resets it back to 'pending' so the runner picks it up
 * again on the next tick. Also clears blockedReason and resets retryCount.
 */
export async function retryBlockedTask(id: string): Promise<{ success: boolean; error?: string }> {
    try {
        const { updateTask, getTask } = await import('@/lib/agent-tasks');
        const { log } = await import('@/lib/autopilot-logger');
        const task = getTask(id);
        if (!task) return { success: false, error: 'Task not found' };
        if (task.state !== 'blocked' && task.state !== 'failed') {
            return { success: false, error: `Task is in state '${task.state}', not blocked/failed` };
        }
        updateTask(id, {
            state:         'pending',
            blockedReason: undefined,
            retryCount:    0,
            result:        undefined,
        });
        log.info('task_started', `🔄 Task manually retried by user.`, { taskId: id });
        return { success: true };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

// ─── OODA Re-Planner ─────────────────────────────────────────────────────────

/**
 * Autonomous re-planning: the agent discovered new context and needs to
 * rewrite/reprioritize/delete pending tasks in tasks.json.
 *
 * Called by the skill-dispatcher's ooda_replan handler.
 */
export async function replanTasksFromContext(options: {
    newContext: string;
    planTitle?: string;
}): Promise<{ success: boolean; summary?: string; changes?: any[]; error?: string }> {
    try {
        const settings = await loadSettings();
        const allTasks = getAllTasks();
        const pending  = allTasks.filter(t => t.state === 'pending');

        if (pending.length === 0) {
            return { success: true, summary: 'No pending tasks to re-plan.', changes: [] };
        }

        const systemPrompt = `You are Skales Autopilot's OODA (Observe-Orient-Decide-Act) re-planner.
New critical context has been discovered that may invalidate or change the current task plan.
Analyze the context and decide which pending tasks to: UPDATE, DELETE, or REPRIORITIZE.

NEW CONTEXT:
${options.newContext}

CURRENT PENDING TASKS:
${pending.map((t, i) => `${i + 1}. [${t.id.slice(0, 8)}] "${t.title}" [${t.priority}]:\n   ${t.description.slice(0, 200)}`).join('\n')}

OUTPUT: Respond with ONLY a JSON array of change directives. Each directive:
{
  "taskId": "first 8 chars of task id",
  "action": "update" | "delete" | "reprioritize",
  "newTitle": "optional new title (for update)",
  "newDescription": "optional new description (for update)",
  "newPriority": "high|normal|low (for reprioritize)",
  "reason": "why this change is needed"
}

If no changes are needed, output: []`;

        const raw = await callLLM(settings, systemPrompt,
            `Re-plan based on this new context: ${options.newContext.slice(0, 500)}`);

        let directives: any[] = [];
        try {
            const cleaned = raw.replace(/^```json\s*/i, '').replace(/```\s*$/, '').trim();
            directives = JSON.parse(cleaned);
            if (!Array.isArray(directives)) directives = [];
        } catch { /* no re-plan needed */ }

        const changes: any[] = [];
        const { log } = await import('@/lib/autopilot-logger');

        for (const d of directives) {
            // Find the full task ID by prefix match
            const task = pending.find(t => t.id.startsWith(d.taskId));
            if (!task) continue;

            if (d.action === 'delete') {
                deleteTask(task.id);
                changes.push({ taskId: task.id, action: 'deleted', reason: d.reason });
                log.warning('plan_created', `🗑️ OODA: Deleted task "${task.title}" — ${d.reason}`, { taskId: task.id });

            } else if (d.action === 'update') {
                updateTask(task.id, {
                    title:       d.newTitle       ?? task.title,
                    description: d.newDescription ?? task.description,
                    replanReason: d.reason,
                    replannedAt:  new Date().toISOString(),
                });
                changes.push({ taskId: task.id, action: 'updated', reason: d.reason });
                log.warning('plan_created', `✏️ OODA: Updated task "${task.title}" — ${d.reason}`, { taskId: task.id });

            } else if (d.action === 'reprioritize') {
                updateTask(task.id, {
                    priority:     d.newPriority ?? task.priority,
                    replanReason: d.reason,
                    replannedAt:  new Date().toISOString(),
                });
                changes.push({ taskId: task.id, action: 'reprioritized', newPriority: d.newPriority, reason: d.reason });
                log.info('plan_created', `🔀 OODA: Reprioritized "${task.title}" → ${d.newPriority} — ${d.reason}`, { taskId: task.id });
            }
        }

        const summary = changes.length > 0
            ? `OODA re-plan: ${changes.length} change(s) made — ${changes.map(c => `${c.action} "${c.taskId.slice(0, 8)}"`).join(', ')}`
            : 'No task changes needed based on new context.';

        return { success: true, summary, changes };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

