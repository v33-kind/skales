const PROFILE_FILE = path.join(DATA_DIR, 'user_profile.json');

// ─── Types ───────────────────────────────────────────────────────────────────

export interface UserProfile {
    /** What the user is ultimately trying to achieve */
    primaryGoal?: string;
    /** Business/personal niche */
    niche?: string;
    /** Available budget / resources */
    budget?: string;
    /** Key constraints (time, legal, technical) */
    constraints?: string;
    /** Anything else Skales should know long-term */
    additionalContext?: string;
    /** The raw strategy/roadmap generated by the last planning session */
    masterPlan?: string;
    /** Title of the current master plan */
    masterPlanTitle?: string;
    /** History of previous roadmaps (newest first, max 10) */
    roadmapHistory?: Array<{ roadmap: string; planTitle: string; createdAt: number }>;
    /** ISO timestamp of last profile update */
    updatedAt?: number | string;
    /** Name the user prefers Skales to use */
    preferredName?: string;
}

export interface MasterPlanTask {
    title:       string;
    description: string;
    priority:    TaskPriority;
    tags?:       string[];
    /** If set, a 5-field cron expression — task becomes a recurring cron job */
    schedule?:   string | null;
}

// ─── User Profile ─────────────────────────────────────────────────────────────

function ensureDir() {
    if (!fs.existsSync(DATA_DIR)) fs.mkdirSync(DATA_DIR, { recursive: true });
}

export async function loadUserProfile(): Promise<UserProfile> {
    try {
        if (!fs.existsSync(PROFILE_FILE)) return {};
        return JSON.parse(fs.readFileSync(PROFILE_FILE, 'utf-8')) as UserProfile;
    } catch {
        return {};
    }
}

export async function saveUserProfile(profile: UserProfile): Promise<{ success: boolean; error?: string }> {
    try {
        ensureDir();
        const existing = await loadUserProfile();
        const merged   = { ...existing, ...profile, updatedAt: new Date().toISOString() };
        fs.writeFileSync(PROFILE_FILE, JSON.stringify(merged, null, 2));
        return { success: true };
    } catch (e: any) {
        return { success: false, error: e.message };
    }
}

